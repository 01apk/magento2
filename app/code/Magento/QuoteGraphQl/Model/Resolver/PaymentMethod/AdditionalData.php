declare(strict_types=1);

namespace Magento\QuoteGraphQl\Model\Resolver\PaymentMethod;

use Magento\Framework\Exception\LocalizedException;
use Magento\Framework\GraphQl\Config\Element\Field;
use Magento\Framework\GraphQl\Query\ResolverInterface;
use Magento\Framework\GraphQl\Schema\Type\ResolveInfo;

/**
 * Class AdditionalData
 *
 * @package Magento\QuoteGraphQl\Model\Resolver\PaymentMethod
 */
class AdditionalData implements ResolverInterface
{
    /**
     * @inheritdoc
     */
    public function resolve(
        Field $field,
        $context,
        ResolveInfo $info,
        array $value = null,
        array $args = null
    ) {
        if (!isset($value['model'])) {
            throw new LocalizedException(__('"model" value should be specified'));
        }
        $payment = $value['model'];

        return [
            'additional_data' => $payment->getAdditionalInformation()
        ];
    }
}
